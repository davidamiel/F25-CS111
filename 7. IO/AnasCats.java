public class AnasCats {

    public static void main(String[] args) {

        StdOut.println("How many cats does Anna have?");
        int anasCats = StdIn.readInt();

        StdOut.println("How many cats does Ellen have?");
        int ellensCats = StdIn.readInt();

        int sumOfCats = anasCats + ellensCats;

        StdOut.println("They have a total of " + sumOfCats + " cats! Wow!");




        
    }

    
}
